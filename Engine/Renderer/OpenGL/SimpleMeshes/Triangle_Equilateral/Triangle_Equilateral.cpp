ABFramework::Triangle_Equilateral::Triangle_Equilateral()
	:Mesh()
{
	m_VertexCount = 3U;
	m_IndexCount = 3U;
	pVertices = new Vertex[m_VertexCount];
	pVertices[0].Set({ -0.5f, -0.5f, 0.0f, 1.0f }, { 0.0f, 0.0f });
	pVertices[1].Set({ 0.0f, 0.5f, 0.0f, 1.0f }, { 0.5f, 0.5f });
	pVertices[2].Set({ 0.5f, -0.5f, 0.0f, 1.0f }, { 1.0f, 0.0f });
	pIndices = new unsigned int[m_IndexCount];
	pIndices[0] = 0U;
	pIndices[1] = 1U;
	pIndices[2] = 2U;

}

ABFramework::Triangle_Equilateral::~Triangle_Equilateral()
{

}


