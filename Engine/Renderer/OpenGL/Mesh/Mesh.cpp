//********************************************************************************//

ABFramework::Mesh::Mesh() 
	: pVertices(nullptr), pIndices(nullptr), m_VertexCount(0U), m_IndexCount(0U)
{

};

ABFramework::Mesh::~Mesh() 
{ 
	delete[] pVertices; 
	pVertices = nullptr;
	delete[] pIndices;
	pIndices = nullptr;
	m_VertexCount = 0U;
	m_IndexCount = 0U;
}

